# step 3: LayerNorm implementation
# 层归一化，提高神经网络训练的稳定性和效率（梯度消失或爆炸）
# 调整神经网络层的输出，使其具有均值为0，方差为1的分布。
# 有助于加速权重的收敛。
# 层归一化是在特征维度上进行的，对每个输入独立进行归一化，不受批次大小的限制。
###################################################################
# we added two trainable parameters, a scale and a shift parameter:
# The initial scale (multiplying by 1) and shift (adding 0) values don't have any effect; 
# however, scale and shift are trainable parameters that the LLM automatically adjusts 
# during training if it is determined that doing so would improve the model's performance 
# on its training task.This allows the model to learn appropriate scaling and shifting 
# that best suit the data it is processing
###################################################################

class LayerNorm(nn.Module):
    def __init__(self, emb_dim):
        super().__init__()
        self.eps = 1e-5
        self.scale = nn.Parameter(torch.ones(emb_dim))
        self.shift = nn.Parameter(torch.zeros(emb_dim))

    def forward(self, x):
        mean = x.mean(dim=-1, keepdim=True)
        var = x.var(dim=-1, keepdim=True, unbiased=False)
        norm_x = (x - mean) / torch.sqrt(var + self.eps)
        return self.scale * norm_x + self.shift
    

